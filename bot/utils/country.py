def get_country_info(country_code: str):
    """
    获取国家信息
    返回: (国旗Emoji, 国家名称)
    """
    # 确保区号以 + 开头
    if not country_code.startswith('+'):
        country_code = '+' + country_code
    
    return COUNTRY_DATA.get(country_code, ("🌍", "未知国家"))


def parse_phone_country_code(phone: str):
    """
    从手机号解析国家区号
    支持格式: +86xxxxxxxxxx, 86xxxxxxxxxx, +86 xxxxxxxxxx
    """
    phone = phone.strip().replace(' ', '').replace('-', '')
    
    # 确保以 + 开头
    if not phone.startswith('+'):
        phone = '+' + phone
    
    # 尝试匹配已知区号（从长到短）
    for code in sorted(COUNTRY_DATA.keys(), key=len, reverse=True):
        if phone.startswith(code):
            return code
    
    # 如果没有匹配，尝试提取前1-4位数字
    import re
    match = re.match(r'\+(\d{1,4})', phone)
    if match:
        return '+' + match.group(1)
    
    return None


def format_price_list(prices):
    """
    格式化价格列表
    prices: [(country_code, country_name, flag_emoji, price), ...]
    """
    if not prices:
        return "暂无价格数据"
    
    text = "📋 <b>当前收购价格</b>\n\n"
    
    for price in sorted(prices, key=lambda x: x[3], reverse=True):  # 按价格排序
        country_code, country_name, flag_emoji, price_value = price
        text += f"{flag_emoji} <code>{country_code}</code> {country_name}: <b>{price_value} USDT</b>\n"
    
    return text
