def emoji(name: str, fallback: str = None) -> str:
    """
    生成动态 Emoji HTML 标签
    
    Args:
        name: Emoji 符号（如 "💰"）
        fallback: 备用显示文本（默认使用 name）
    
    Returns:
        HTML 格式的自定义 Emoji 标签
    
    Example:
        emoji("💰") -> '<tg-emoji emoji-id="5377505475015235101">💰</tg-emoji>'
    """
    if fallback is None:
        fallback = name
    
    emoji_id = EMOJI_IDS.get(name)
    if not emoji_id:
        # 如果没有对应的动态 Emoji，直接返回原符号
        return name
    
    return f'<tg-emoji emoji-id="{emoji_id}">{fallback}</tg-emoji>'

